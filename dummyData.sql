            max(t.fechacreacion) AS max_fechacreacion
           FROM jseguimientostareas t
          WHERE t.tituloseguimientotarea::text = 'Tarea rechazada'::text
          GROUP BY t.idtarea) latest ON latest.idtarea = tsk.idtarea;