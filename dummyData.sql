-- public.vcobrosindebios

CREATE OR REPLACE VIEW public.vcobrosindebios
AS SELECT tck.idtarea AS ticket,
    tck.codigo,
    corp.ruc AS ruccorporation,
    cod.codigotipoidentificacion AS tipoidentificacionsujeto,
    cli.identificacion AS identificacionsujeto,
    (cli.nombre::text || ' '::text) || cli.apellido::text AS nomapellidonomrazonsocial,
    can.codigocanalrecepcion AS canalrecepcion,
    pro.fechacreacion AS fecharecepcion,
    tptran.codigotipotransaccion AS tipotransaccion,
    con.codigoconcepto AS concepto,
        CASE
            WHEN tck.idestado <= 6 THEN 1
            ELSE 2
        END AS estadoreclamo,
    COALESCE(res.fecharesolucion, '0001-12-31 23:40:40-05:19:20 BC'::timestamp with time zone) AS fecharespuesta,
    COALESCE(tr.codigotiporesoulcion, ''::character varying) AS tiporesolucion,
    COALESCE(val.monto, 0::numeric) AS montorestituido,
    COALESCE(val.interesmonto, 0::numeric) AS interesmonto,
    COALESCE(val.totalmonto, 0::numeric) AS totalrestituido
   FROM jpersonas cli
     JOIN jtiposidentificaciones cod ON cod.idtipoidentificacion = cli.idtipoidentificacion
     JOIN jproblemas pro ON pro.idpersona = cli.idpersona
     JOIN jcanalesrecepciones can ON can.idcanalrecepcion = pro.idcanalrecepcion
     JOIN jtareasticket tck ON tck.idproblema = pro.idproblema
     JOIN jtipostransacciones tptran ON tptran.idtipotransaccion = pro.idtipotransaccion
     JOIN jconceptos con ON con.idconcepto = pro.idconcepto
     JOIN jsucursales suc ON suc.idsucursal = pro.idsucursal
     JOIN jcorporaciones corp ON corp.idcorporacion = suc.idcorporacion
     JOIN jestados es ON es.idestado = tck.idestado
     LEFT JOIN jresoluciones res ON res.idtarea = tck.idtarea
     LEFT JOIN jvaloresresoluciones val ON val.idresolucion = res.idresolucion
     LEFT JOIN jtiporesoluciones tr ON tr.idtiporesolucion = res.idtiporesolucion
  WHERE tck.indicador::text = 'P'::text;

